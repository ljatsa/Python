import turtle
t = turtle.Turtle()
aken = turtle.Screen()
aken.title("Olümpiarõngad - Lisett-Rosette")
aken.setup(500,500)
t.speed(0) #Kiiruse muutmine
t.pensize(10)
#vars
t.penup()
t.goto(0,-200)
t.pendown()
t.pencolor("green")
t.lt(90)
t.fd(200)
#õis
t.begin_fill()
t.color("blue", "lightblue")
t.rt(90)
t.circle(60)
t.end_fill()
t.penup()
t.goto(0,50)
t.pendown()
t.pencolor("yellow")
t.pensize(20)
t.circle(10)
#leht
t.penup()
t.goto(0,-130)
t.pendown()
t.pencolor("green")
t.lt(35)
t.fd(40)
t.rt(35)
t.lt(-35)
t.fd(-40)
t.rt(-35)
t.rt(90)
t.fd(35)


turtle.down()